if (! function_exists('is_mobile')) {
    /**
     * 判断当前设备是否是移动设备
     */
    function is_mobile()
    {
        //如果有HTTP_X_WAP_PROFILE则一定是移动设备
        if (isset($_SERVER['HTTP_X_WAP_PROFILE'])) {
            return true;
        }
        //如果via信息含有wap则一定是移动设备,部分服务商会屏蔽该信息
        if (isset($_SERVER['HTTP_VIA'])) {
            //找不到为flase,否则为true
            return stristr($_SERVER['HTTP_VIA'], 'wap') ? true : false;
        }
        //脑残法，判断手机发送的客户端标志,兼容性有待提高
        if (isset($_SERVER['HTTP_USER_AGENT'])) {
            $clientkeywords = [
                'nokia',
                'sony',
                'ericsson',
                'mot',
                'samsung',
                'htc',
                'sgh',
                'lg',
                'sharp',
                'sie-',
                'philips',
                'panasonic',
                'alcatel',
                'lenovo',
                'iphone',
                'ipod',
                'blackberry',
                'meizu',
                'android',
                'netfront',
                'symbian',
                'ucweb',
                'windowsce',
                'palm',
                'operamini',
                'operamobi',
                'openwave',
                'nexusone',
                'cldc',
                'midp',
                'wap',
                'mobile',
            ];
            //从HTTP_USER_AGENT中查找手机浏览器的关键字
            if (preg_match('/(' . implode('|', $clientkeywords) . ')/i', strtolower($_SERVER['HTTP_USER_AGENT']))) {
                return true;
            }
        }

        //协议法，因为有可能不准确，放到最后判断
        if (isset($_SERVER['HTTP_ACCEPT'])) {
            // 如果只支持wml并且不支持html那一定是移动设备
            // 如果支持wml和html但是wml在html之前则是移动设备
            if ((strpos($_SERVER['HTTP_ACCEPT'], 'vnd.wap.wml') !== false) && (strpos($_SERVER['HTTP_ACCEPT'], 'text/html') === false || (strpos($_SERVER['HTTP_ACCEPT'], 'vnd.wap.wml') < strpos($_SERVER['HTTP_ACCEPT'], 'text/html')))) {
                return true;
            }
        }

        return false;
    }
}


if (! function_exists('str_filter')) {
    /*
     * 去除字符串中的非法字符，sql注入等
     * @param $char：要过滤的字符串
     * @param $type：字符串类型，string(default)、 html
     * @return string
     */
    function str_filter($char, $type = 'string')
    {
        if ($char === null || $char === false) {
            return $char;
        }
        $char = trim($char);   //去掉两边空格
        //字符串
        if ($type == 'string') {
            $char = urldecode($char);   //url解码：Get方式传递的值有的浏览器会自动urlencode()
            $char = strip_tags($char);  //去掉字符串中的HTML、XML、PHP标签
            $char = htmlspecialchars($char);    //预定义的字符转换为HTML:',",&,>,<
            $char = addslashes($char);   //为预定义字符添加反斜杠:',",\,NULL
        } //html
        else if ($type == 'html') {
            $char = preg_replace("(\\\\+')", "'", $char);  //连续反斜杠加单引号，处理为单引号
            $char = str_replace("'", "\'", $char);  //单引号添加反斜杠
            $char = str_replace('"', "\\\"", $char);  //单引号添加反斜杠
            $char = str_replace("<?", "&lt;?", $char);  //转义php的声明符
            $char = str_replace("?>", "?&gt;", $char);
            $char = str_replace("<script", "&lt;script", $char);  //转义script声明符
            $char = str_replace("</script>", "&lt;/script&gt;", $char);
            // $char = preg_replace('/\r|\n|\t/', '', $char);
        } else if ($type == 'json') {

        }
        return $char;
    }
}

if (! function_exists('decode_str2arr')) {
    function decode_str2arr($arr)
    {
        foreach ($arr as $key => $value) {
            $value = str_replace(array("\r\n", "\r", "\n"), "", $value);
            $value_tmp = json_decode($value, true);
            if (is_array($value_tmp)) {
                $arr[$key] = $value_tmp;
            }
        }
        return $arr;
    }
}


if (! function_exists('distance_format')) {
    /**
     * @param int $distance
     *
     * @return int|string
     */
    function distance_format($distance = 0)
    {
        $val = round($distance, 2);

        if ($val < 0) {
            return '-';
        }

        if ($val == 0) {
            return '<1m';
        }

        if ($val < 1) {
            $val *= 1000;

            return $val . 'm';
        }

        return $val . 'km';
    }
}


if (! function_exists('get_distance')) {
    /**
     * 求两个已知经纬度之间的距离,单位为米
     *
     * @param lng1 $ ,lng2 经度
     * @param lat1 $ ,lat2 纬度
     *
     * @return float 距离，单位米
     */
    function get_distance($lng1, $lat1, $lng2, $lat2)
    {
        // 将角度转为狐度
        $radLat1 = deg2rad($lat1); //deg2rad()函数将角度转换为弧度
        $radLat2 = deg2rad($lat2);
        $radLng1 = deg2rad($lng1);
        $radLng2 = deg2rad($lng2);
        $a       = $radLat1 - $radLat2;
        $b       = $radLng1 - $radLng2;
        $s       = 2 * asin(sqrt(pow(sin($a / 2), 2) + cos($radLat1) * cos($radLat2) * pow(sin($b / 2), 2))) * 6378.137 * 1000;
        $s       = $s < 1000 ? $s . 'm' : round($s / 1000, 2);

        return $s;
    }
}

